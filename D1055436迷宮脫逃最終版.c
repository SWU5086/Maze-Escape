//遊戲主體邏輯函數
void Game()
{
	int d,e;
    n=UP; //開始遊戲時，默認向前移動
    int tmp=0; //記錄移動方向
    //goto first; //第一次進入循環先向默認方向前進
    while (1)
    {
        d=head.x-body.x;
		e=head.y-body.y;
        n=getch(); //讀取鍵值
        //在執行前，需要對所讀取的按鍵進行調整
        switch (n)
        {
        case UP://按前進 
            Movehead(d,e,1);
            weaponrun();
            break;
        case DOWN: //按後退
            Moveback(d,e);
            weaponrun();
            break;
        case LEFT:
        	if(d==0&&e==-1)//朝前 
            	Movehead(-1,1,0);
			else if(d==0&&e==1)//朝後 
            	Movehead(1,-1,0);
			else if(d==-1&&e==0)//朝左 
            	Movehead(1,1,0);
			else if(d==1&&e==0)//朝右 
            	Movehead(-1,-1,0);
			weaponrun();	
            break;
        case RIGHT: 
			if(d==0&&e==-1)//朝前 
            	Movehead(1,1,0);
			else if(d==0&&e==1)//朝後 
            	Movehead(-1,-1,0);
			else if(d==-1&&e==0)//朝左 
            	Movehead(1,-1,0);
			else if(d==1&&e==0)//朝右 
            	Movehead(-1,1,0);	
            weaponrun();
            break;
        case SPACE:
        	system("pause"); //暫停後按任意鍵繼續
            break;
        case ESC:
        	system("cls"); //清空視窗 
            color(14); //顏色設為黃色
            CursorJump(COL/3, ROW / 2);
            printf("  遊戲結束  ");
            exit(0);
        case 'r':
        	system("cls"); //清空屏幕
            main(); //重新執行主程式 
            break; 
        default:
            break;
        }
    }
}
