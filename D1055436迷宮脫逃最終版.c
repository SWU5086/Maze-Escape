//用C語言開發遊戲程序時，對於游標閃爍問題，可以通過隱藏游標解決
void HideCursor()
{
    CONSOLE_CURSOR_INFO curInfo; //定義游標信息的結構體變量，標頭檔<windows.h>
    curInfo.dwSize =1; //如果沒賦值的話，游標不隱藏
    curInfo.bVisible=0; //將游標設置為不可見
    HANDLE handle = GetStdHandle(STD_OUTPUT_HANDLE); //獲取控制台
    SetConsoleCursorInfo(handle, &curInfo); //設置游標信息
}
//游標跳轉
void CursorJump(int x, int y)//可以選擇印出的位置 
{
    COORD pos; //定義游標位置的結構體變量
    pos.X = x; //橫坐標
    pos.Y = y; //縱坐標
    HANDLE handle = GetStdHandle(STD_OUTPUT_HANDLE); //獲取控制台
    SetConsoleCursorPosition(handle, pos); //設置游標位置
} 
